                <p>For our manufacturing process we use dry sunflower seeds having reduced moisture content up to 9%.</p>
                <p>Crude sunflower oil process includes:</p>
                <p>- shelling and separation of shells in a series of crackers and bed screens,
                    leaving about 8-12% shells in the kernels and a maximum of 1.5%
                    kernels in the shells. Shells, later o,  are compacted and used as fuel
                    for boilers.</p>
                <p>- After the shelling, the seeed content
                    is transported to the large volume brazier
                    in which the contes gets heat up and steam under the pressure.</p>
                <p>- Crude oil is obtained through mechanical pressing only. In the
                    practise, we use two subsequent screw presses  together with
                    continuous heating and moisturizing coming from the boilers.</p>
                <p>The 1st pressing is effected in an expeller, a large screw press,
                    which extracts up to 35% of the oil, while through 2nd pressing,
                    extraction reaches up to 55%. Pressing, without solvent extraction,
                    can be practised either on decorticated or non decorticated seeds</p>
                <p>- The oil is then filtered and ready for refining</p>
