            <div class="modal-body">
                <img src="./img/offer2.jpg" alt="" class="modal_image img-fluid">
                <span class="modal_body_title">Refining inludes:</span>
                <p>Degumming. The sunflower oilseeds are rich sources of phospholipids. Phospholipids may pose many problems for the storage and processing of the crude oil and they are removed from oil during refining by degumming. Most of the phospholipids in crude sunflower hydratable and can be removed by water degumming by heating the oil to 80 C, mixing with water (4-5% vol) and stirring 15 min and subsequent centrifugation.</p>
                <p>Naturalization. The presence of free fatty acids in oil may promote oxidation. The presence of high levels of free fatty acids, or free acidity, is due to wet harvest conditions which promote the action of lipases generating these molecules, as well as moist grains during storage. Neutralization is an important step in the edible oil refining process and aims to remove FFA in oil. It neutralizes free fatty acids in the oil using caustic soda and potash, also decomposes pigments, it eliminates phosphorus compounds, trace metals, proteins and oxidizing materials. Alkali treatment converts the acids into soaps. These soaps are easily removed by decantation or by centrifugation.</p>
                <p>Bleaching. Another  oil refining process which has main objective to remove coloring pigments from carotenoids, chlorophylls, and related compounds that catalyze the oxidation of the oil by reacting with light, by using bleaching earths that can be acid activated or coupled with activated carbon to increase the adsorption power and diminish the quantities used. Bleaching earth are also adsorbing residual soaps, phospholipids and other polar lipids</p>
                <p>Dewaxing eliminates long chain waxes (with carbon number higher than C42) and saturated triglycerides by reducing the oil’s temperature to 6–8 ◦C and eliminating the solid particles, for instance, by filtration. Dewaxing also reduces the least the minor compounds and particularly tocopherols. In sunflower oil, the reduction in total tocopherol content range from 0.6 to 5.8% with an average of about 2.4%. Dewaxing is made by a winterisation process which cools the oil to crystallise the wax and removes the wax crystals by filtration. • If not dewaxed, the oil may appear cloudy and wax may settle out. For consumer-grade cooking and the manufacture of food products oil must be fully refined and winterised.</p>
